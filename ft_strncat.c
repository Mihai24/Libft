#include "libft.h"

char	*ft_strncat(char *dest, char *src, size_t n)
{
	char	*s;

	s = dest;
	while (*s != '\0')
		s++;
	while ((n > 0) && (*src != '\0'))
	{
		*s++ = *src++;
		n--;
	}
	*s = '\0';
	return (dest);
}
