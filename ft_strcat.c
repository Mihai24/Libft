#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	char	*str;

	str = dest;
	while (*str != '\0')
		str++;
	while (*src != '\0')
		*str++ = *src++;
	*str = '\0';
	return (dest);
}
